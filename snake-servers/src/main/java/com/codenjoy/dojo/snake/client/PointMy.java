package com.codenjoy.dojo.snake.client;

import java.awt.*;

public class PointMy {

    int x;
    int y;


    public PointMy(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public PointMy() {
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
